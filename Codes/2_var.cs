using System;

namespace _2_var
{
    class var
    {
        static void Main(string[] args)
        {
            //Αλφαριθμητικό
            string s = "DIT";
            Console.WriteLine(s);

            //Ακέραιος αριθμός
            int i = 100;
            Console.WriteLine(i);

            //Λογική πρόταση
            bool t = true;
            Console.WriteLine(t);

            //Χαρακτήρας
            char k = 'k';
            Console.WriteLine(k);

            //Αλλαγή γραμμής
            Console.WriteLine("\n");

        }
    }
}

using System;

namespace math
{
    class math
    {
        static void Main(string[] args)
        {
            //Εύρεση μεγίστου
            Console.WriteLine(Math.Max(32, 10));

            //Εύρεση ελαχίστου
            Console.WriteLine(Math.Min(32, 10));

            //Εύρεση ρίζας
            Console.WriteLine(Math.Sqrt(32));

            //Εύρεση απόλυτης τιμής
            Console.WriteLine(Math.Abs(-30.5));

            //Στρογγυλοποίηση
            Console.WriteLine(Math.Round(58.8));


        }
    }
}
